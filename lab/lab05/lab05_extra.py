def add_trees(t1, t2):
    """
    >>> numbers = tree(1,
    ...                [tree(2,
    ...                      [tree(3),
    ...                       tree(4)]),
    ...                 tree(5,
    ...                      [tree(6,
    ...                            [tree(7)]),
    ...                       tree(8)])])
    >>> print_tree(add_trees(numbers, numbers))
    2
      4
        6
        8
      10
        12
          14
        16
    >>> print_tree(add_trees(tree(2), tree(3, [tree(4), tree(5)])))
    5
      4
      5
    >>> print_tree(add_trees(tree(2, [tree(3)]), tree(2, [tree(3), tree(4)])))
    4
      6
      4
    >>> print_tree(add_trees(tree(2, [tree(3, [tree(4), tree(5)])]), \
    tree(2, [tree(3, [tree(4)]), tree(5)])))
    4
      6
        8
        5
      5
    """
    "*** YOUR CODE HERE ***"
    if is_leaf(t1):
        return tree(label(t1)+label(t2), branches(t2))
    elif is_leaf(t2):
        return tree(label(t1)+label(t2), branches(t1))
    bs_1, bs_2 = branches(t1), branches(t2)  # branches已经是一个list了
    num = min(len(bs_1), len(bs_2))  # 看哪一个的branch更少，以最少的为基准匹配
    new_bs = [add_trees(i, j) for i, j in list(zip(bs_1, bs_2))]
    add_term = bs_1[num:] if len(bs_1) > num else bs_2[num:]  # 确定要加的是哪一棵branch更多的数的多余部分的branch
    return tree(label(t1)+label(t2), new_bs + add_term)



